# Data Types in Python
# 1. int: Integer type, used to represent whole numbers (e.g., 1, -5, 42).
# 2. float: Floating-point type, used to represent decimal numbers (e.g., 3.14, -0.5, 2.0).
# 3. str: String type, used to represent text (e.g., "Hello", "Python", "123").
# 4. bool: Boolean type, used to represent True or False values.
# 5. list: List type, used to represent a collection of items (e.g., [1, 2, 3], ["apple", "banana"]).
# 6. tuple: Tuple type, similar to lists but immutable (e.g., (1, 2, 3), ("apple", "banana")).
# 7. dict: Dictionary type, used to represent key-value pairs (e.g., {"name": "Alice", "age": 25}).
# 8. set: Set type, used to represent a collection of unique items (e.g., {1, 2, 3}, {"apple", "banana"}).
# 9. NoneType: Represents the absence of a value (e.g., None).
# 10. complex: Complex number type, used to represent complex numbers (e.g., 3 + 4j).