# Variable Naming Conventions
# 1. Variable names can only contain letters, numbers, and underscores.
# 2. Variable names cannot start with a number.
# 3. Variable names are case-sensitive (e.g., "name" and "Name" are different variables).
# 4. Variable names should be descriptive and meaningful.
# 5. Avoid using reserved keywords (e.g., "if", "else", "while", etc.) as variable names.
# 6. Use lowercase letters for variable names, and separate words with underscores (e.g., "first_name").
# 7. Use camelCase for variable names if you prefer (e.g., "firstName").
# 8. Use meaningful names that describe the purpose of the variable (e.g., "total_price" instead of "tp").
# 9. Avoid using single-letter variable names (except for loop counters).
# 10. Use plural names for lists or collections (e.g., "students" instead of "student_list").
# 11. Use "is_" or "has_" prefixes for boolean variables (e.g., "is_active", "has_permission").
# 12. Use "get_" prefix for functions that return a value (e.g., "get_user_info").
# 13. Use "set_" prefix for functions that modify a value (e.g., "set_user_name").
# 14. Use "calculate_" prefix for functions that perform calculations (e.g., "calculate_total").
# 15. Use "print_" prefix for functions that print output (e.g., "print_user_info").